    private void excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirActionPerformed
        // TODO add your handling code here:
        Cursos curso = localizaCurso();
        if (curso != null) {
            try {
                new Controller_Curso().excluirCurso(curso, sessão);
                JOptionPane.showMessageDialog(null, "Curso excluído com sucesso.");
                excluirLinhasTabela();
                carregaTabela();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "O Curso não pode ser excluído.");
                Logger.getLogger(Cursos_Cadastrados.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_excluirActionPerformed

    private void alterar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterar1ActionPerformed
        // TODO add your handling code here:
        Cursos lc = localizaCurso();
        if (lc != null) {
            new Novo_Curso(this.sessão, lc).setVisible(true);
            JOptionPane.showMessageDialog(null, "Curso alterado com sucesso.");
            excluirLinhasTabela();
            carregaTabela();
        }
    }//GEN-LAST:event_alterar1ActionPerformed

