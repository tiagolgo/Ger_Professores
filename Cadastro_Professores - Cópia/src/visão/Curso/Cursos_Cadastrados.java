    private void excluirLinhasTabela() {
        DefaultTableModel model = (DefaultTableModel) this.tabela_Cursos.getModel();
        int linhas = this.tabela_Cursos.getRowCount();
        while (linhas > 0) {
            model.removeRow(0);
            linhas--;
        }
    }

    private void carregaTabela() {
        DefaultTableModel model = (DefaultTableModel) this.tabela_Cursos.getModel();

        this.cursos = new Controller_Curso().retornaCursos();
        if (this.cursos != null) {
            for (Cursos curso : this.cursos) {
                model.addRow(new Object[]{
                    curso.getId(),
                    curso.getCodigo(),
                    curso.getNome(),
                    curso.getTurno()
                });
            }
        } else {
            int op = JOptionPane.showConfirmDialog(null, "Não há Cursos cadastrados.\nDeseja Cadastrar?", "Confirmação", 0);
            if (op == 0) {
                new Novo_Curso().setVisible(true);
                carregaTabela();
            } else {
                this.dispose();
            }
        }
    }

