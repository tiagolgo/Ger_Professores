    private Cursos localizaCurso() {
        int linha;
        if ((linha = this.tabela_Cursos.getSelectedRow()) == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um Curso!");
        } else {
            Object id = this.tabela_Cursos.getValueAt(linha, 0);
            if (id != null) {
                for (Cursos curso : cursos) {
                    if (id.equals(curso.getId())) {
                        return curso;
                    }
                }
            }
        }
        return null;
    }
